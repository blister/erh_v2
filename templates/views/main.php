			<p>
				I started PHPainfree on April 21st, 2010 to win an argument with 
				<a href="https://twitter.com/walesmd">Mike Wales</a>. At the time,
				he was a hardcore evangelist for <a href="https://codeigniter.com">Code Igniter</a>
				and I was <span class="sidenote-anchor"></span>
				<span class="sidenote">... and still am</span>
				a proponent of "get-the-hell-out-of-my-way-and-just-let-me-write-code" as a 
				primary philosophy.
			</p>
			<p>
				While my stance has softened (somewhat), I still firmly believe that 
				the software engineering industry has a very bad habit of rigid adherence to 
				frameworks to our detriment. Having worked professionally from the late 90s to 
				today, I've seen frameworks come and go, and the only thing they seem to ever bring 
				to the table is a guarantee of technical debt. Today's React app is yesterday's Ruby 
				on Rails.
			</p>
			<h4>We Are Programmers</h4>
			<p>
				I wanted a "framework" that stayed out of my way and just let me focus on writing 
				code, so PHPainfree was born. Written to be intentionally small and minimalistic, 
				I've successfully used PHPainfree to be my secret weapon for the past 14 years. Whenever 
				I need to build a web-based product, PHPainfree has let me build something fast, scalable, and 
				capable of doing just what needs to be done and not spend hours fighting with opaque 
				documentation and bizarre framework methods.
			</p>
			<aside>
				<h3>Enter htmx</h3>
				<p>
					<a href="https://htmx.org">htmx</a> is to Javascript frameworks as PHPainfree is to web frameworks. It's such a great 
					system that I went and built a second version of PHPainfree to natively support htmx structures 
					out of the box.
				</p>
			</aside>
			<p>
				With the power and speed of PHP version 8, speedy development environments powered by 
				<a href="https://docker.com">Docker</a>, and lightweight modern interactivity provided by 
				<a href="https://htmx.org">what HTML should have added to the specification</a>, PHPainfree2 was 
				born.
			</p>
			<p>
				Get it today at <a href="https://php.programming-is-easy.com">php.programming-is-easy</a> and 
